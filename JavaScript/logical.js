// LOGICAL OPERATORS are of three typeof
//Logical AND (&&)
// Results true if both the operands are true
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false)
// Logical OR (||)
// Returns True if only one of the two operand is true
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);
// NOT(!) operator is used to convert true into false and false into true
console.log(!true);
console.log(!false);