// 1- Arithmetic Operators
let x = 53;
let y = 58;
console.log(x+y); //Performs addition
console.log(x-y); //Performs subtraction
console.log(x*y); //Performs multiplication
console.log(x/y); //Divides x by Y
console.log(x%y); //Gives reminder for x divide by y
console.log(x**y); //Raises x to power of y
// Increment Operator (++) 
// Increment operator too are of 2 types they are pre(++x) and post(x++) increments
console.log(++x);
console.log(x++)
console.log(x);
// Decrement operator are too of two types they are pre(--x) and post(x--) decrements
console.log(--x);
console.log(x--);
console.log(x)
